							<p>Nearly 6 out of 10 organisations face a shortage of expertise</p>
							<p>Cylander partner ING is proud to announce a unique partnership in the fight against cybercrime. ING, Microsoft and (OWASP) are set to launch a common digital platform aimed at training future experts in IT risks and security. The platform will go live in early 2022 with the support of the Open Source Security Foundation. The aim of this initiative is to remedy the structural shortage of skilled labour in the domain which, according to a recent study, impacts 57% of organisations in Belgium. The partnership was announced at the “Risk & Security in the New Normal” conference in the presence of Minister for Justice, Vincent Van Quickenborne.</p>
							<p>Cybercrime currently affects all private and public organisations. The number of computer viruses and cyber-attacks has never been higher. Banking sector statistics showed that some 67,000 fraudulent phishing transactions were perpetrated in Belgium in 2020 at a cost of 34 million euros</p>
							<p>The impact of this scourge weighs on the entire economy.</p>
							<p>Philippe Ledent, economist at ING in Belgium</p>
							<p>“The financial cost of cybercrime is no longer just impacting businesses but the global economy. Different sources estimate the cost of online crime on global GDP at USD 1 trillion,”</p>
							<p>In order to fight this form of crime, organisations need IT risk and security specialists. However, a recent study has revealed that 57% of organisations face a shortage of skilled personnel in this domain. Three quarters (76%) of professionals questioned also mentioned having persistent difficulties in hiring qualified profiles. It is in this urgent context that the partners of Cylander have decided to join forces to launch a series of training materials aimed at preparing future IT risk and security experts to anticipate and counter cyber-attacks. This will take the form of a shared training platform to be launched by all partners in early 2022 and will be universally accessible. The content of the training will be designed by the partners themselves based on their experience and needs.</p>
