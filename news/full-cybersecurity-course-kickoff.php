							<p>Cylander is thrilled to announce that its first certified cyber security course has officially kicked off in Mombasa, Kenya. After a rigorous selection process, a group of nearly 30 talented and motivated students have been hand-selected to take part in this intensive one-year programme, designed to prepare them for careers as certified Hack The Box penetration testers.</p>
							<p>The kickoff marks a landmark moment for Cylander and its partners. What began as a shared vision between Cylander, ING, Close the Gap Kenya, and Hack The Box has now become a reality on the ground. The energy in the room during the first sessions was remarkable, with students eager to dive into the world of ethical hacking, vulnerability assessment, and cyber defence.</p>
