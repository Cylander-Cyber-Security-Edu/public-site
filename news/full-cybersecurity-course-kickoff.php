							<h2>Building Africa's next generation of cyber security professionals</h2>
							<p>The demand for skilled cybersecurity professionals continues to grow rapidly across the African continent and globally. With an estimated 3.5 million unfilled cybersecurity positions worldwide, programmes like this are not just valuable, they are essential. By investing in local talent, Cylander and its partners are helping to close the skills gap while empowering young professionals to protect critical digital infrastructure in their communities and beyond.</p>
							<p>Each student in the programme was carefully selected based on their aptitude, dedication, and potential to make a meaningful impact in the field. Over the course of the next twelve months, they will work through a comprehensive curriculum that combines hands-on labs, real-world scenarios, and mentorship from experienced professionals.</p>
							<h2>A strong partnership driving real impact</h2>
							<p>This initiative showcases what is possible when the private sector, non-profit organisations, and the tech community come together around a common goal. ING brings its deep commitment to corporate social responsibility and cybersecurity expertise. Close the Gap Kenya provides the local infrastructure and community connections essential for reaching the right candidates. Hack The Box delivers a world-class training platform that is trusted by security teams and professionals around the globe.</p>
							<p>Together, these partners have created a programme that is not only free of charge for participants but also delivers training that meets international industry standards. This model of collaboration is one that Cylander hopes to replicate in other regions in the future.</p>
